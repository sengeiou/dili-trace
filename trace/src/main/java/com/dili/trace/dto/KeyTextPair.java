package com.dili.trace.dto;

public class KeyTextPair {
    private Object key;
    private String text;

    public Object getKey() {
        return key;
    }

    public void setKey(Object key) {
        this.key = key;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
