package com.dili.trace.domain.hangguo;

/**
 * @author asa.lee
 */
public class HangGuoCommodity {
    private String firstCateg;
    private String secondCateg;
    private String categoryNumber;
    private String itemNumber;
    private String itemName;
    private String itemUnitName;

    public String getFirstCateg() {
        return firstCateg;
    }

    public void setFirstCateg(String firstCateg) {
        this.firstCateg = firstCateg;
    }

    public String getSecondCateg() {
        return secondCateg;
    }

    public void setSecondCateg(String secondCateg) {
        this.secondCateg = secondCateg;
    }

    public String getCategoryNumber() {
        return categoryNumber;
    }

    public void setCategoryNumber(String categoryNumber) {
        this.categoryNumber = categoryNumber;
    }

    public String getItemNumber() {
        return itemNumber;
    }

    public void setItemNumber(String itemNumber) {
        this.itemNumber = itemNumber;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public String getItemUnitName() {
        return itemUnitName;
    }

    public void setItemUnitName(String itemUnitName) {
        this.itemUnitName = itemUnitName;
    }
}
